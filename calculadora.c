#include<math.h>

double adicao(double a, double b){
    double soma;
    soma = a + b;
    return soma;
}

double subtracao(double a, double b){
    double sub;
    sub = a - b;
    return sub;
}

double potenciacao(double a, double b){
    double potencia;
    potencia = pow(a, b);
    return potencia;
}

double raiz_quadrada(double a){
    double raiz;
    raiz = sqrt(a);
    return raiz;
}


